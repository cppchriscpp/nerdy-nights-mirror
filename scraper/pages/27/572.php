Just go to Graphics &gt; Image Helper Tool.<br>
Then File &gt; Load Image.<br>
Choose the BMP.<br>
Then when you&apos;re sure it&apos;s looking decent you can export the CHR, NAM, PAL, ATTR, etc, etc.&#xA0; It&apos;s fantastic.<br>
