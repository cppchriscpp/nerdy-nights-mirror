2 - Can&apos;t binary represent letters as well? For example, on <a href="http://www.nickciske.com/tools/binary.php" target="_blank">this site</a> you can convert binary to text and back again, which can hold letters, numbers, and punctuation.</begin></div><br>
The <a href="http://www.asciitable.com/" target="_blank">ascii table</a> is the standard conversion between a number and a letter or other character.  The main benefits of ascii are that the letters are sequential (which seems like an obviously good idea) and you can change between lower case and upper case by adding or subtracting the same number.  One byte holds almost all characters including punctuation.  It is slowly being replaced by unicode, which uses more than one byte so it can do things like Chinese characters.
